# python中类方法，类实例方法，静态方法有何区别？
'''
类方法：是类对象的方法，在定义时需要在上方使用@classmethod进行装饰，形参为cls，表示类对象。类对象和实例对象都可以调用。
类实例方法：是类实例化对象的方法，只有实例对象才可以调用，形参为self，指代对象本身。
静态方法：是一个任意函数，在其上方使用@staticmethod进行装饰，可以用对象直接调用，实际静态方法和类无明显关系。
'''
