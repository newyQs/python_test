# 什么是可变和不可变数据类型？
'''
可不可变指的是内存中的值是否可以被改变。不可变类型指的是对象所在内存块里面的值不可以被改变，有数值，字符串，元组。
而可变类型则是可以改变，主要有列表，字典。
'''

