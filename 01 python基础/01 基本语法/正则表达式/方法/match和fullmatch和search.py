import re

# match(pattern, string, flags=0)
# returning a Match object, or None if no match was found.
re.match()

# fullmatch(pattern, string, flags=0)
# returning a Match object, or None if no match was found.
re.fullmatch()

# search(pattern, string, flags=0)
# returning a Match object, or None if no match was found.
re.search()
